package com.Lib;

public class Library {
	private int id;
	private String name;
	public Library(int id, String name) {
		super();
		this.id=id;
		this.name=name;
	}
	public int getId(){
		return id;
	}
	public void setId(int id) {
		this.id=id;
	}
	public String getname(){
		return name;
	}
	public void setname(String name) {
		this.name=name;
	}
	
	

}
